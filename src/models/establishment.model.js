class Establishment {
    _id;
    _name;
    _cep;
    _street;
    _streetNumber;
    _streetComplement;
    _neighborhood;
    _city;
    _state;
    
    get id() {
        return this._id;
    }
    set id(value) {
        this._id = value;
    }
    
    get name() {
        return this._name;
    }
    set name(value) {
        this._name = value;
    }
    
    get cep() {
        return this._cep;
    }
    set cep(value) {
        this._cep = value;
    }
    
    get street() {
        return this._street;
    }
    set street(value) {
        this._street = value;
    }
    
    get streetNumber() {
        return this._streetNumber;
    }
    set streetNumber(value) {
        this._streetNumber = value;
    }
    
    get streetComplement() {
        return this._streetComplement;
    }
    set streetComplement(value) {
        this._streetComplement = value;
    }
    
    get neighborhood() {
        return this._neighborhood;
    }
    set neighborhood(value) {
        this._neighborhood = value;
    }
    
    get city() {
        return this._city;
    }
    set city(value) {
        this._city = value;
    }

    get state() {
        return this._state;
    }
    set state(value) {
        this._state = value;
    }
}